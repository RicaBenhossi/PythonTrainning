"""
OPOERAÇÕES EM NÍVEL DE BITS

Operações no nível dos bits são operações que manipulam diretamente os bits.
"""
# Operadores de bits

# Desloca para a Direita >>
print(5 >> 4)
# Desloca para a Esquerda <<
print(5 << 1)
# AND no nível dos bits &
print(8 & 5)
# OR no nível dos bits |
print(9 | 4)
# XOR no nível dos bits ^
print(12 ^ 42)
# NOT no nível dos bits ~
print(~88)